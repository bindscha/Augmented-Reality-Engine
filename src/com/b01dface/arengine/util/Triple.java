package com.b01dface.arengine.util;

/**
 * A generic triple class.
 * 
 * @author Laurent Bindschaedler
 */
public class Triple<E1, E2, E3> {

	public final E1 _1;
	public final E2 _2;
	public final E3 _3;
	
	public Triple(E1 _1, E2 _2, E3 _3) {
		this._1 = _1;
		this._2 = _2;
		this._3 = _3;
	}
	
}
